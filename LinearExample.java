// Example of Linear Time Complexity - O(n)


public class LinearExample {
    public static void main(String[] args) {
        int[] arr = {1, 2, 3, 4, 5};/*"Iterating through an array	Time increases linearly with input size.." */
        for (int i = 0; i < arr.length; i++) {
            System.out.println(arr[i]);  // Printing each element (linear time)
        }
    }
}
